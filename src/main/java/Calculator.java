public class Calculator {
     int add(final int number1, final int number2) {
        int sum;
        sum = number1 + number2;
        return sum;
    }


    int sub(final int number1, final int number2) {
        int sub;
        sub = number1 - number2;
        return sub;
    }
    int mul(final int number1, final int number2) {
        int mul;
        mul = number1 * number2;
        return mul;
    }
    double div(final double number1, final double number2) {
        double div;
        div = number1 / number2;
        return div;
    }
}

